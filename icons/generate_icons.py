def render_icon(name, spec):
    """Render a pixel-art icon spec to a 12x12 PNG."""
    img = Image.new('RGB', (12, 12), (0, 0, 0))
    draw = ImageDraw.Draw(img)
    color = spec['color']
    dim_color = tuple(c // 3 for c in color)

    for y, row in enumerate(spec['pixels']):
        for x, ch in enumerate(row):
            if ch == '‚Ėą':
                draw.point((x, y), fill=color)
            elif ch == '‚ĖĎ':
                draw.point((x, y), fill=dim_color)
            # space = black (background)

    img.save(f'icons/{name}.png')
    print(f'Generated icons/{name}.png')


